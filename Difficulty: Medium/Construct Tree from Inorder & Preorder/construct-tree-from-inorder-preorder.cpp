class Solution {
  public:
    // Function to build the tree from given inorder and preorder traversals
    Node* f(int i1,int i2,vector<int>& pr,vector<int>& in,int& p1){
        if (i1 > i2) return nullptr;
        int el=pr[p1];
        p1++;
        int i=i2;
        while(i >= i1 && in[i] != el) {
            i--;
        }
        Node* temp=new Node(el);
        temp->left=f(i1,i-1,pr,in,p1);
        temp->right=f(i+1,i2,pr,in,p1);
        return temp;
    }
    Node *buildTree(vector<int> &in, vector<int> &pr) {
        int p1=0;
        int n=in.size();
        return f(0,n-1,pr,in,p1);
    }
};

