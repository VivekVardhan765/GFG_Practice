class Solution {
  public:
    void fr(Node* root,int key,Node*& r){
        if(!root)return;
        if(root->data>key){
            if(r->data > root->data){
                r=root;
                fr(root->left,key,r);
            }
        }
        else if(root->data<key){
            fr(root->right,key,r);
        }
        else{
            fr(root->right,key,r);
        }
    }
    void fl(Node* root,int key,Node*& l){
        if(!root)return;
        if(root->data<key){
            if(l->data < root->data){
                l=root;
                fl(root->right,key,l);
            }
        }
        else if(root->data>key){
                fl(root->left,key,l);
        }
        else{
            fl(root->left,key,l);
        }
    }
    
    vector<Node*> findPreSuc(Node* root, int key) {
        Node* l = new Node(-1e7);
        Node* r = new Node(1e7);

        fl(root,key,l);
        fr(root,key,r);
        if(r->data==1e7)r->data=-1;
        if(l->data==-1e7)l->data=-1;
        return {l,r};
    }
};


